package com.wenbin.moodinnpro.entity;


public class Admins {

  private int admimid;
  private String adminno;
  private String adminemail;
  private String adminpwd;
  private String adminname;
  private String adminhead;
  private String admintitle;


  public int getAdmimid() {
    return admimid;
  }

  public void setAdmimid(int admimid) {
    this.admimid = admimid;
  }


  public String getAdminno() {
    return adminno;
  }

  public void setAdminno(String adminno) {
    this.adminno = adminno;
  }


  public String getAdminemail() {
    return adminemail;
  }

  public void setAdminemail(String adminemail) {
    this.adminemail = adminemail;
  }


  public String getAdminpwd() {
    return adminpwd;
  }

  public void setAdminpwd(String adminpwd) {
    this.adminpwd = adminpwd;
  }


  public String getAdminname() {
    return adminname;
  }

  public void setAdminname(String adminname) {
    this.adminname = adminname;
  }


  public String getAdminhead() {
    return adminhead;
  }

  public void setAdminhead(String adminhead) {
    this.adminhead = adminhead;
  }


  public String getAdmintitle() {
    return admintitle;
  }

  public void setAdmintitle(String admintitle) {
    this.admintitle = admintitle;
  }

}
